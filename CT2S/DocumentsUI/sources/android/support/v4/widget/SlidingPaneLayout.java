    private static boolean viewIsOpaque(View v) {
        Drawable bg;
        if (ViewCompat.isOpaque(v)) {
            return true;
        }
        return Build.VERSION.SDK_INT < 18 && (bg = v.getBackground()) != null && bg.getOpacity() == -1;
    }

